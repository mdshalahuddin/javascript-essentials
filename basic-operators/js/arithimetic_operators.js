// 1. Arithmetic Operators (for basic calculations)

let a = 10;

let b = 3;

let c = 5;

// 1. Addition (add two or more numbers)

const sum = a + b;

console.log("Sum of (a  + b) =", sum); // 13

// 2. Subtraction (subtract two or more numbers)

const difference = a - b - c; 

console.log("Subtraction: ", difference) // 7



// 3. Multiplication( multiply two or more numbers)

const product = b * c;
console.log("Multiplication: ", product) // 15

// 4. Division (divide two numbers)

const result = (a / b); //(10 / 3)
console.log("Division: ", result) // 3.3333334


// Modulus (remainder)

const remainder = a % b;

console.log("Remainder: ", remainder)  // 1


