// Comparison Operators (used for decisions making)

let number1 = '20'; // string
let number2 = 20; // number


// 1. Equal to (pertial (==) or strict equality (===))

const isEqual = (number1 === number2); // 10=20
console.log(isEqual)


// 2. Not equal to ( != )

let a = 5;
let b = 3;
console.log("Not equal to: ", a != b)

// 3. Greater than (5 > 3)

console.log("Greater than: ", a > b)

//4. Less than (5 < 3)

console.log("Less than: ", a < b)

// 5. Greater than or equal

console.log("Greater or equal: ", a >= b) // 5 > 3 or 5 == 3

// 6. Less than or equal

console.log("Less than or equal: ", a <= b) // 5 < 3 or 5 == 3