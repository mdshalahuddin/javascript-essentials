// How to create Variable?

/*
3 types of variables

1. var: old style (not used)

2. let

3. const
*/ 

// 1. using var
var name = "Mamun"

name = "Joy"

console.log(name)

// 2. using let (when value changes)

let age = 25;

age = 30;

console.log(age)

// 3. using const (used for fixed value)
const country =  "Bangladesh"

const country2 = "United States"

console.log(country)
console.log(country2)




